# Создаем словарь с именами и годами рождения известных людей
известные_люди = {
    "Эйнштейн": 1879,
    "Моцарт": 1756,
    "Шекспир": 1564,
    "Кьюри": 1867,
    "Ганди": 1869
}

# Инициализируем переменные для подсчета результатов
правильные_ответы = 0
ошибки = 0

# Проходимся по словарю и задаем вопросы
for человек, год in известные_люди.items():
    ответ = input(f"Введите год рождения {человек}: ")
    try:
        ответ = int(ответ)
        if ответ == год:
            правильные_ответы += 1
        else:
            ошибки += 1
    except ValueError:
        ошибки += 1

# Считаем проценты
всего_вопросов = len(известные_люди)
процент_правильных = (правильные_ответы / всего_вопросов) * 100
процент_ошибок = (ошибки / всего_вопросов) * 100

# Выводим результаты
print(f"Количество правильных ответов: {правильные_ответы}")
print(f"Количество ошибок: {ошибки}")
print(f"Процент правильных ответов: {процент_правильных}%")
print(f"Процент ошибок: {процент_ошибок}%")
